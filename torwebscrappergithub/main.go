func getUserInput(prompt string) string {
	fmt.Print(prompt)
	var input string
	fmt.Scanln(&input)
	return input
}

func saveHTMLToFile(htmlContent []byte, filename string) error {
	err := ioutil.WriteFile(filename, htmlContent, 0644)
	if err != nil {
		return err
	}
	return nil
}

func MustParseURL(rawurl string) *url.URL {
	u, err := url.Parse(rawurl)
	if err != nil {
		panic(err)
	}
	return u
}
